public class exam03 {
    public static void main(String[] args) {
        int a = 10;
        int b = 3;

        System.out.println("a+b = " + (a+b));
        System.out.println("a+b = " + (a+b * 2));
        System.out.println("a+b = " + ((a+b) * 2));



        int c = 1;
        System.out.println( c );
        System.out.println( ++c );  // ++c -> 증가 시키고 c를 출력
        System.out.println( c++);   // c++ -> c를 출력하고 증가시키기
        System.out.println( c );


    }
}
